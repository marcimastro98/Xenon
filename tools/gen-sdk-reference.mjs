#!/usr/bin/env node
// Generates the canonical "Capability reference" block in docs/WIDGET_SDK.md from
// the single source of truth in server/sdk-widgets.js (SDK_STREAMS +
// SDK_ACTION_CATEGORIES). The block is delimited by SDK-REFERENCE markers.
//
//   node tools/gen-sdk-reference.mjs          # rewrite the block in place
//   node tools/gen-sdk-reference.mjs --check  # exit 1 if the block is stale (CI/test)
//
// Run it after changing the SDK stream/action allowlists. `npm test` enforces it
// (server/test/sdk-docs-sync.test.mjs), so the docs can never silently drift from
// what the SDK actually exposes.
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { createRequire } from 'node:module';

const require = createRequire(import.meta.url);
const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const DOC = join(ROOT, 'docs', 'WIDGET_SDK.md');
const { SDK_STREAMS, SDK_ACTION_CATEGORIES } = require(join(ROOT, 'server', 'sdk-widgets.js'));

const START = '<!-- SDK-REFERENCE:START (auto-generated by tools/gen-sdk-reference.mjs â€” do not edit by hand) -->';
const END = '<!-- SDK-REFERENCE:END -->';

function buildBlock() {
  const streams = [...SDK_STREAMS].sort();
  const cats = Object.keys(SDK_ACTION_CATEGORIES).sort();
  const lines = [];
  lines.push('### Capability reference (auto-generated)');
  lines.push('');
  lines.push('The exact set the SDK exposes today, generated from the code. Request');
  lines.push('these in your manifest `streams` / `actions`; the host only forwards what');
  lines.push('the user granted, and every action is re-validated server-side.');
  lines.push('');
  lines.push('**Data streams** (`streams`): ' + streams.map((s) => '`' + s + '`').join(', '));
  lines.push('');
  lines.push('**Action categories** (`actions`) â†’ the action `type`s each unlocks:');
  lines.push('');
  lines.push('| Category | Action types |');
  lines.push('|----------|--------------|');
  for (const c of cats) {
    lines.push('| `' + c + '` | ' + SDK_ACTION_CATEGORIES[c].map((t) => '`' + t + '`').join(', ') + ' |');
  }
  return lines.join('\n');
}

function render(doc, block) {
  const s = doc.indexOf(START);
  const e = doc.indexOf(END);
  if (s === -1 || e === -1 || e < s) {
    throw new Error('SDK-REFERENCE markers not found in ' + DOC + ' â€” add them where the block should live.');
  }
  return doc.slice(0, s + START.length) + '\n' + block + '\n' + doc.slice(e);
}

const doc = readFileSync(DOC, 'utf8');
const next = render(doc, buildBlock());
const check = process.argv.includes('--check');

// A Windows checkout materialises the doc with CRLF (core.autocrlf), while the
// block we generate is always LF â€” so a byte comparison reports STALE on every
// Windows machine even when the content matches, and `npm test` fails out of a
// clean clone. Compare on normalised newlines (real drift only), and write back
// in whatever convention the file already uses so regenerating never churns it.
const eol = doc.includes('\r\n') ? '\r\n' : '\n';
const norm = (s) => s.replace(/\r\n/g, '\n');

if (norm(next) === norm(doc)) {
  console.log('SDK reference block is up to date.');
  process.exit(0);
}
if (check) {
  console.error('SDK reference block is STALE. Run: node tools/gen-sdk-reference.mjs');
  process.exit(1);
}
writeFileSync(DOC, eol === '\r\n' ? norm(next).replace(/\n/g, '\r\n') : norm(next));
console.log('Updated SDK reference block in docs/WIDGET_SDK.md.');
